package day03_VariablesContinue;

public class ArithmeticOperators {

    public static void main(String[] args) {
        System.out.println(5+3);
        System.out.println(5-3);
        System.out.println(5*3);
        System.out.println(5/3);
        System.out.println(5%3);
        /*
        remainder from the division
        10/4=2.5 reminder:numerator-(denominator* whole number)
        10-(4*2)=2
         */
    }
}
