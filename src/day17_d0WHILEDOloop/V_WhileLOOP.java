package day17_d0WHILEDOloop;

public class V_WhileLOOP {
    public static void main(String[] args) {

        for(int i=0; i<=5; i++){
            System.out.println(i);}
        System.out.println("===========================");
        int i =0;
        while(i<=5){
            System.out.println(i);
            i++;

        }
    }
}
