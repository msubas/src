package Day18_NestedLOOP;

public class XIV_NestedLOOP3 {

    public static void main(String[] args) {

     for(int j=0; j<=9; j++){
        for(int i=0; i<=j; i++){
           System.out.print("* ");
       } System.out.println();}


         for(int m=0; m<=9; m++){
             for(int i=9; i>=m; i--){
                 System.out.print("* ");
             }
             System.out.println();}}}

