package Day16_LOOPS;

public class IV_MorningTask {
    public static void main(String[] args) {

        for (char z ='Z'; z >='A'; z--){
            System.out.print(z+" "); }


        for (char a ='A'; a<='Z'; a++){
            System.out.print("\n"+"\t"+a+" "); }
   }}

