package SundayStudy;



public class Mathoperations {
    public static void main(String[] args) {


        int x=10;
        int y=2;

        System.out.println(x + " plus " + y+ " equals " + (x+y));
        System.out.println(x + " minus " + y+ " equals " + (x-y));
        System.out.println(x + " times " + y+ " equals " + (x*y));
        System.out.println(x + " divided " + y+ " equals " + (x/y));
        System.out.println(x + " diveded " + y+ " and reminder is " + (x%=y));




    }
}
