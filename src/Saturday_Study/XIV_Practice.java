package Saturday_Study;

public class XIV_Practice {
    public static void main(String[] args) {


        String a = "abba";
        String b = "";

        for (int i = a.length()-1; i >=0; i--) {
            b += a.charAt(i); }


        if (a.equals(b)) {
            System.out.println("Word is a palindrome");}
        else {
            System.out.println("Word is not a palindrome");
       }}}

